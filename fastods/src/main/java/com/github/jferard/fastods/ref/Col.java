    public static int parseString(final String letters) {
        if (letters.isEmpty()) {
            throw new IllegalArgumentException();
        }
        final List<Integer> alphaPositions = letters.chars().mapToObj(c -> (char) c).map(codePoint -> Character.toUpperCase(codePoint) - PositionUtil.ORD_A).collect(Collectors.toList());
        if (alphaPositions.stream().anyMatch(c -> c < 0 || c > 25)) {
            throw new IllegalArgumentException();
        }

        int pos = 0;
        for (int i = 0; i < alphaPositions.size() - 1; i++) {
            final int alphaPos = alphaPositions.get(i);
            pos = pos * 26 + alphaPos + 1;
        }
        return pos * 26 + alphaPositions.get(alphaPositions.size() - 1);
    }
